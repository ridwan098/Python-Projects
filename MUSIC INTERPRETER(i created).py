# music note interpreter

loop = 1 == 1
while loop == True:

    note = input('Enter your notes:')

    if note == 'LowC':
        print('0001')
    if note == 'D':
        print('0010')
    if note == 'E':
        print('0011') 
    if note == 'F':
        print('0100') 
    if note == 'G':
        print('0101')
    if note == 'A':
        print('0110')
    if note == 'B':
        print('0111')
    if note == 'HighC':
        print('1000')
    print(note)


